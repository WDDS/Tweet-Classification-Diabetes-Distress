def get_parrotsEmotions_list_all():
    """
        Return list with all primary, secondary and third emotions from Parrot
        iterate over ParrotsEmotionsTree
    """
    ParrotsEmotions = []
    for primaryEmot, secondaryEmot in ParrotsEmotionsTree.items():
        ParrotsEmotions.append(primaryEmot)
        for secondaryEmo, thirdEmo in secondaryEmot.items():
            ParrotsEmotions.append(secondaryEmo)
            ParrotsEmotions.extend(thirdEmo)

    return list(set(ParrotsEmotions))


def get_parrotsEmotions_for_emtion(emotion):
    """
        Return list with all primary, secondary and third emotions from Parrot
        starting from the given 'emotion'

        parameters
        ----------------------------------------------------------------
        emotion:        ['love', 'joy', 'surprise', 'fear', 'anger', 'sad']

        return
        -----------------------------------------------------------------------
        list of all synonyms of the given emotion
    """
    ParrotsEmotions = [emotion]
    for primaryEmot, secondaryEmot in ParrotsEmotionsTree[emotion].items():
        ParrotsEmotions.append(primaryEmot)
        ParrotsEmotions.extend(secondaryEmot)

    try:
        ParrotsEmotions.remove("")
    except:
        pass

    return list(set(ParrotsEmotions))


def all_word_forms(wordlist):
    """
        For each word in wordlist get the other word forms:
        adverb 'r', adjective 'a', noun 'n' and verb 'v'

        For ex.:
        >>> get_word_forms("elect")
        >>> {'n': {'elector', 'elects', 'electors', 'elective', 'electorates', 'elect', 'electives', 'elections', 'electorate', 'eligibility', 'election', 'eligibilities'},
             'r': set(),
             'a': {'elect', 'electoral', 'elective', 'eligible'},
             'v': {'elect', 'elects', 'electing', 'elected'}}

        parameters
        ------------------------------------------------
        wordlist:       list of words
    """

    list_all = []
    for word in wordlist:
        list_all.append(word)
        for k, v in get_word_forms(word).items():
            list_all.extend(list(v))

    try:
        list_all.remove('')
    except:
        pass

    return list(set(list_all))


def get_synonym(word):
    """
        Get synonyms of the given word using wordnet
    """

    syns = wordnet.synsets(word)
    return list(set([l.name() for s in syns for l in s.lemmas()]))


def get_synonyms(wordlist, excludeSynonyms, addWords):
    """
        get all synonyms for the given wordlist using wordnet and return a list
        containing all words of wordlist and its synonyms

        parameters
        -------------------------------------------------------------------
        wordlist:           list with word tokens
        excludeSynonyms:    list with words that are to be excluded from the list
                            due to their generality like go, get, etc.
        addWords:           list of words to be added
    """
    list_all=[]
    for word in wordlist:
        list_all.extend(get_synonym(word))

    list_all = list(set(list_all))

    # exclude certain words
    for word in excludeSynonyms:
        try:
            list_all.remove(word)
            #print("excluded {}".format(word))
        except:
            #print("Info: Could not exclude {}".format(word))
            pass

    # add certain words
    for word in addWords:
        list_all.append(word)

    return list_all



